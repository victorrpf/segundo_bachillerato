# Declarar un array (lista) de frutas
frutas = ["plátano", "fresa", "cereza"]
print("Lista de frutas:", frutas)

# Añadir un elemento al array
frutas.append("naranja")
print("Después de append:", frutas)

# Acceder a un elemento específico del array
print("Cuarta fruta:", frutas[3])

# Añadir un número a la lista de frutas
numero = 1
frutas.append(numero)
print("Después de añadir un número:", frutas)
# en Python las listas no están “tipadas” por elemento, son contenedores dinámicos y pueden guardar valores de cualquier tipo

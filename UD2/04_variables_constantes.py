# Variables (mutables por reasignación)
nombre = "María"
edad = 17
nota_media = 8.5

print("Nombre:", nombre)
print("Edad:", edad)
print("Nota media:", nota_media)

# Constantes (convención: mayúsculas)
PI = 3.14159
MAX_ALUMNOS = 30
print("PI:", PI, "MAX_ALUMNOS:", MAX_ALUMNOS)

# Variables (mutables por reasignación)
nombre = "María"
edad = 17
nota_media = 8.5
aprobado = True

print("Nombre:", nombre)
print("Edad:", edad)
print("Nota media:", nota_media)
print("Aprobado:", aprobado)

# Mostrar tipo inferido por Python usando type()
print()
print("tipo(nombre):", type(nombre))
print("tipo(edad):", type(edad))
print("tipo(nota_media):", type(nota_media))
print("tipo(aprobado):", type(aprobado))

# Variable con valor None, lo que en otros lenguajes sería null
sin_valor = None
print("sin_valor:", sin_valor)
print("tipo(sin_valor):", type(sin_valor))

# Constantes (convención: mayúsculas)
PI = 3.14159
MAX_ALUMNOS = 30
print("PI:", PI, "MAX_ALUMNOS:", MAX_ALUMNOS)
print("tipo(PI):", type(PI))
print("tipo(MAX_ALUMNOS):", type(MAX_ALUMNOS))
